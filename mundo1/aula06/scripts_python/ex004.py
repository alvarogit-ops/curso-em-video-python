txt = input("\033[1;36mDigite qualquer coisa: \033[m")

print("\033[0;36mÉ maiúsculo?\033[1;32m")
print(f"\033[m{txt.isupper()}\033[m")
print("\033[0;36mÉ minúsculo?\033[1;32m")
print(f"\033[m{txt.islower()}\033[m")
print("\033[0;36mÉ título?\033[m")
print(txt.istitle())
print("\033[0;36mÉ identificador de usuário?\033[0m")
print(txt.isidentifier())
